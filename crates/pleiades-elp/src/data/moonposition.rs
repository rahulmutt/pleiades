//! Meeus-style truncated lunar position formulas for the Moon.
//!
//! This module ports the compact Chapter 47 lunar position algorithm used by
//! several open-source Meeus implementations. It computes geocentric ecliptic
//! longitude, latitude, and distance for a given TT Julian day, using the
//! published truncated periodic series for the Moon's longitude, latitude, and
//! range.

use pleiades_types::{Latitude, Longitude};

const J2000: f64 = 2_451_545.0;
const AU_IN_KM: f64 = 149_597_870.700;
const DEG_TO_RAD: f64 = std::f64::consts::PI / 180.0;

pub fn position(jde: f64) -> (Longitude, Latitude, f64) {
    let t = (jde - J2000) / 36_525.0;
    let l_prime = horner(
        t,
        &[
            218.316_447_7,
            481_267.881_234_21,
            -0.001_578_6,
            1.0 / 538_841.0,
            -1.0 / 65_194_000.0,
        ],
    ) * DEG_TO_RAD;

    let (d, m, m_prime, f) = dmf(t);
    let a1 = (119.75 + 131.849 * t) * DEG_TO_RAD;
    let a2 = (53.09 + 479_264.29 * t) * DEG_TO_RAD;
    let a3 = (313.45 + 481_266.484 * t) * DEG_TO_RAD;
    let e = horner(t, &[1.0, -0.002_516, -0.000_007_4]);
    let e2 = e * e;

    let mut sigma_l = 3_958.0 * a1.sin() + 1_962.0 * (l_prime - f).sin() + 318.0 * a2.sin();
    let mut sigma_r = 0.0;
    let mut sigma_b = -2_235.0 * l_prime.sin()
        + 382.0 * a3.sin()
        + 175.0 * (a1 - f).sin()
        + 175.0 * (a1 + f).sin()
        + 127.0 * (l_prime - m_prime).sin()
        - 115.0 * (l_prime + m_prime).sin();

    for &(dd, mm, mp, ff, sl, sr) in TA {
        let arg = dd * d + mm * m + mp * m_prime + ff * f;
        let sin_arg = arg.sin();
        let cos_arg = arg.cos();
        match mm as i32 {
            0 => {
                sigma_l += sl * sin_arg;
                sigma_r += sr * cos_arg;
            }
            1 | -1 => {
                sigma_l += sl * sin_arg * e;
                sigma_r += sr * cos_arg * e;
            }
            2 | -2 => {
                sigma_l += sl * sin_arg * e2;
                sigma_r += sr * cos_arg * e2;
            }
            _ => unreachable!("Meeus lunar longitude terms only use M coefficients of 0, ±1, ±2"),
        }
    }

    for &(dd, mm, mp, ff, sb) in TB {
        let arg = dd * d + mm * m + mp * m_prime + ff * f;
        let sin_arg = arg.sin();
        match mm as i32 {
            0 => sigma_b += sb * sin_arg,
            1 | -1 => sigma_b += sb * sin_arg * e,
            2 | -2 => sigma_b += sb * sin_arg * e2,
            _ => unreachable!("Meeus lunar latitude terms only use M coefficients of 0, ±1, ±2"),
        }
    }

    let longitude =
        Longitude::from_degrees((l_prime.to_degrees() + sigma_l * 1.0e-6).rem_euclid(360.0));
    let latitude = Latitude::from_degrees(sigma_b * 1.0e-6);
    let distance_km = 385_000.56 + sigma_r * 1.0e-3;
    let distance_au = distance_km / AU_IN_KM;

    (longitude, latitude, distance_au)
}

fn dmf(t: f64) -> (f64, f64, f64, f64) {
    let d = horner(
        t,
        &[
            297.850_192_1,
            445_267.111_403_4,
            -0.001_881_9,
            1.0 / 545_868.0,
            -1.0 / 113_065_000.0,
        ],
    ) * DEG_TO_RAD;
    let m = horner(
        t,
        &[
            357.529_109_2,
            35_999.050_290_9,
            -0.000_153_5,
            1.0 / 24_490_000.0,
        ],
    ) * DEG_TO_RAD;
    let m_prime = horner(
        t,
        &[
            134.963_396_4,
            477_198.867_505_5,
            0.008_741_4,
            1.0 / 69_699.0,
            -1.0 / 14_712_000.0,
        ],
    ) * DEG_TO_RAD;
    let f = horner(
        t,
        &[
            93.272_095,
            483_202.017_523_3,
            -0.003_653_9,
            -1.0 / 3_526_000.0,
            1.0 / 863_310_000.0,
        ],
    ) * DEG_TO_RAD;
    (d, m, m_prime, f)
}

fn horner(t: f64, coeffs: &[f64]) -> f64 {
    coeffs
        .iter()
        .rev()
        .copied()
        .fold(0.0, |acc, coeff| acc * t + coeff)
}

