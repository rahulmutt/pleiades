mod tests {
    use super::*;

    #[test]
    fn published_example_matches_pymeeus_reference() {
        let (longitude, latitude, distance_au) = position(2_448_724.5);
        assert!((longitude.degrees() - 133.162_655).abs() < 1e-6);
        assert!((latitude.degrees() - -3.229_126).abs() < 1e-6);
        assert!(((distance_au * AU_IN_KM) - 368_409.7).abs() < 0.2);
    }
}
